import java.util.Scanner;
public class pyramid_3
{
  public static void main(String[]args)
  {
    int i,j;
    for(i=1; i<=7;  i++)
    {  
      for(j=i; j<=4; j++)
    {
      System.out.print(" ");
    } 
     for(j=1;j<=2*1;j++)
     {
      System.out.println("*"); 
     }
     System.out.println();
   }
  }
}
